#include <iostream>
using namespace std;

int main()
{
    // Modulus operator is used to find the remainder of the division.
    // means y under root x. the remainder is the answer.

    int x = 5, y = 2;
    cout << x % y << endl;

    // some important things about modulus operator.
    // if a < b  then the ans is a. like

    int a = 5, b = 7;
    cout << a % b << endl; // ans is a = 5

    // modulus operator is used in divisions and we can also find that the two
    // digits are divisible or not. if the remainder is 0 then the two numbers are
    // divisible.
}