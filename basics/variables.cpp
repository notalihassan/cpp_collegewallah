#include <iostream>

using namespace std;

int main()
{
    int x; // declaration
    x = 5; // initilization
    cout << x << endl;

    int y;
    y = 25;
    cout << y - x << endl;

    cout << "y - x = " << y - x << endl;

    // int means integer and in this case x and y are the variables & we can also writ variable like int x = 5; to declare and initilize the variable in one line.

    int a = 45;
    int b = 2;

    cout << a * b << endl;

    // we can also write the variable in one line like this:-

    int c = 5, d = 3;
    cout << c - d << endl;
}