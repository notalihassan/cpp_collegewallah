#include <iostream>
using namespace std;

int main()
{
    float x = 5, y = 2;
    cout << x + y << endl; // 7
    cout << x - y << endl; // 3
    cout << x * y << endl; // 10
    cout << x / y << endl; // 2.5

    // the ans is in decimal points or floating numbers.more examples are given below.

    float a = 5.567, b = 2.589;
    cout << a + b << endl; // 8.156
    cout << a - b << endl; // 2.978
    cout << a * b << endl; // 14.413
    cout << a / b << endl; // 2.15025
}