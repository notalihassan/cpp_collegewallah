#include <iostream>
using namespace std;

int main()
{

    // in this program we will take input from user and then we will print the square of that number. by using cin and cout. which is c input and c output.

    int x;

    cout << "Enter the number you want to square : ";

    cin >> x; // In cin we use >> right arrows.

    cout << "The square of that number is : ";

    cout << x * x << endl;
}